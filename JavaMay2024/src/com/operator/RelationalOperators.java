package com.operator;

public class RelationalOperators {

	public static void main(String[] args) {
		
		// >, <, >=, <=, ==, !=
		
		int a = 20;
		int b = 20;
		int c = 30;
		
		System.out.println(a > b);
		System.out.println(a < b);
		System.out.println(a >= b);
		System.out.println(a <= b);
		System.out.println(a == b);
		System.out.println(a != b);
	}

}
